export const getTranslation = (lang: Language, key: string): string => {
  const keys = key.split('.');
  let result: Translations | string = translations[lang];
  
  for (const k of keys) {
    if (typeof result === 'object' && result !== null && k in result) {
      result = result[k];
    } else {
      // Fallback to English
      result = translations.en;
      for (const fallbackKey of keys) {
        if (typeof result === 'object' && result !== null && fallbackKey in result) {
          result = result[fallbackKey];
        } else {
          return key;
        }
      }
      break;
    }
  }
  
  return typeof result === 'string' ? result : key;
};

export const getCurrentLanguage = (): Language => {
  const stored = localStorage.getItem('shadowtalk-language');
  if (stored && stored in translations) {
    return stored as Language;
  }
  const browserLang = navigator.language.split('-')[0];
  return browserLang in translations ? (browserLang as Language) : 'en';
};

export const setLanguage = (lang: Language): void => {
  localStorage.setItem('shadowtalk-language', lang);
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
};
